package suwon.web.vo;

public class WeekWeatherVO {
	private String tm; // 발표시간
	private String numEf; // <numEf>2</numEf> 2일후 예보
	private String tmEf; // <tmEf>2010-03-10</tmEf> yyyy-mm-dd 년월일 시간태그
	private String wf; // <wf>구름많고 눈</wf> 날씨예보 태그
	private String tmn; // <tmn>-2</tmn> 최저온도 태그
	private String tmx; // <tmx>4</tmx> 최고온도 태그
	
	private String dayOfWeek; // 요일
	
	public String getTm() {
		return tm;
	}
	public void setTm(String tm) {
		this.tm = tm;
	}
	public String getNumEf() {
		return numEf;
	}
	public void setNumEf(String numEf) {
		this.numEf = numEf;
	}
	public String getTmEf() {
		return tmEf;
	}
	public void setTmEf(String tmEf) {
		this.tmEf = tmEf;
	}
	public String getWf() {
		return wf;
	}
	public void setWf(String wf) {
		this.wf = wf;
	}
	public String getTmn() {
		return tmn;
	}
	public void setTmn(String tmn) {
		this.tmn = tmn;
	}
	public String getTmx() {
		return tmx;
	}
	public void setTmx(String tmx) {
		this.tmx = tmx;
	}
	public String getDayOfWeek() {
		return dayOfWeek;
	}
	public void setDayOfWeek(String dayOfWeek) {
		this.dayOfWeek = dayOfWeek;
	}
}
