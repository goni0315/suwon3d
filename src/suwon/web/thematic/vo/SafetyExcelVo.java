package suwon.web.thematic.vo;

public class SafetyExcelVo {	
	
	private String name;
	private String pointX;
	private String pointZ;
	private String pointY;
	private String modelNo;
	private String lightSize;
	
	
	public SafetyExcelVo(String name,String pointX, String pointZ, String pointY, String modelNo, String lightSize){
		setName(name);		
		setPointX(pointX);
		setPointZ(pointZ);
		setPointY(pointY);
		setModelNo(modelNo);
		setLightSize(lightSize);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPointX() {
		return pointX;
	}

	public void setPointX(String pointX) {
		this.pointX = pointX;
	}

	public String getPointZ() {
		return pointZ;
	}

	public void setPointZ(String pointZ) {
		this.pointZ = pointZ;
	}

	public String getPointY() {
		return pointY;
	}

	public void setPointY(String pointY) {
		this.pointY = pointY;
	}
	
	public String getModelNo() {
		return modelNo;
	}
	
	public void setModelNo(String modelNo) {
		this.modelNo = modelNo;
	}

	public String getLightSize() {
		return lightSize;
	}

	public void setLightSize(String lightSize) {
		this.lightSize = lightSize;
	}
}


